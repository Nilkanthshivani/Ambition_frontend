class ItemWithQty {
  final String id;
  final String name;
  final num length;
  final num width;
  final num height;
  final num weight;
  final int qty;

  ItemWithQty({
    required this.id,
    required this.name,
    required this.length,
    required this.width,
    required this.height,
    required this.weight,
    required this.qty,
  });
}
